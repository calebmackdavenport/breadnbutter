import { Router } from 'express';
import { sendEmail } from '../services/email.svc';

const router = Router();

router.post('/', (req,res)=>{
    sendEmail( 'stinson.lyon@gmail.com', req.body.from, req.body.subject, req.body.message, req.body.name)
    .then((response: any)=>{
        res.sendStatus(201);
    }).catch((e:any)=>{
        console.log(e);
        res.sendStatus(500);
    });
});

export default router;